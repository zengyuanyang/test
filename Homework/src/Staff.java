/**
  *  员工类，姓名，工号，工资
 * @author 曾远洋
 *
 */
public class Staff {
	String name;	//姓名
	int staff_no;	//工号
	int salary;		//工资
	public Staff() {
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getStaff_no() {
		return staff_no;
	}
	public void setStaff_no(int staff_no) {
		this.staff_no = staff_no;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	

}
